import { expect } from 'chai'
import { TEST_ADDRESS, TEST_TX } from './test-defaults'
import { Polygon, Mumbai } from '../../../src'

describe('Polygon Chain', () => {
  it('getChainId', () => {
    expect(Polygon.chainId).to.equal(137)
    expect(Mumbai.chainId).to.equal(80001)
  })

  it('getChainName', () => {
    expect(Polygon.chainName).to.eq('Polygon')
    expect(Mumbai.chainName).to.eq('Mumbai')
  })

  it('isTestChain', () => {
    expect(Polygon.isTestChain).to.be.false
    expect(Mumbai.isTestChain).to.be.true
  })

  it('isLocalChain', () => {
    expect(Polygon.isLocalChain).to.be.false
    expect(Mumbai.isLocalChain).to.be.false
  })

  it('getExplorerAddressLink', () => {
    expect(Polygon.getExplorerAddressLink(TEST_ADDRESS)).to.eq(`https://polygonscan.com/address/${TEST_ADDRESS}`)
    expect(Mumbai.getExplorerAddressLink(TEST_ADDRESS)).to.eq(`https://mumbai.polygonscan.com/address/${TEST_ADDRESS}`)
  })

  it('getExplorerTransactionLink', () => {
    expect(Polygon.getExplorerTransactionLink(TEST_TX)).to.eq(`https://polygonscan.com/tx/${TEST_TX}`)
    expect(Mumbai.getExplorerTransactionLink(TEST_TX)).to.eq(`https://mumbai.polygonscan.com/tx/${TEST_TX}`)
  })
})
